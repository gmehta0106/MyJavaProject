package javasessions;

public class DataTypes {

	public static void main(String[] args) {
		//Primitive data types - these data types don't need any object creation and they occupy the pre-defined memory(memory size is fixed) 
		//Boolean, Char, Integer- byte, short, int, long, Floating type- float and double 
		//1 byte =8 bits
		//byte = 1 byte
		//short = 2 bytes
		//integer = 4 bytes
		//long = 8 bytes
		//float = 4 bytes
		//double = 8 bytes
		//boolean = 1 bit
		//char = 2 bytes
		
		
		//non-primitive data types - Where memory size is not fixed and object creation is needed
		//String, Array, Interface, Class
		
boolean b = true;
System.out.println(b);

char c= 'b';
System.out.println(c);

double d = 2343.888;
System.out.println(d);

int i = 23;
System.out.println(i);

float f = 120f;
System.out.println(f);

long l = 100l;
System.out.println(l);

short s = -10101;
System.out.println(s);

byte b1 = -127;
System.out.println(b1);


	}

}
