package SwitchCases;

public class SwtichCaseEcomm {

	public static void main(String[] args) {
		String MultiCountries = "rr";
		switch (MultiCountries) {
		case "India":
			System.out.println("Indian first language is hindi");
			break;
		case "Canada":
			System.out.println("Canadian first language is French");
			break;
		case "US":
			System.out.println("US first language is American English");
			break;
		default:
			System.out.println("Pls pass the right country");
		}
	}

}
